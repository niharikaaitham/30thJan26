package dataTypes;

public class DataTypeStudy {
    static void main(String[] args) {
        System.out.println("===============================");
        byte wickets;  //variable declaration
        wickets = 125;        //variable assignment/initialization
        System.out.println("Total wickets are " + wickets);  //printing
        short runs;
        runs = 9999;
        System.out.println("Total Runs are " + runs);
        int age;
        age = 99;
        System.out.println("Age is " + age);

        long total;
        total = 999999999999999999L;
        System.out.println("Total is " + total);
        double highScore;
        highScore = 9999.999999d;
        System.out.println("highScore is " + highScore);
        float total_average;
        total_average = 2.4f;
        System.out.println("Average is " + total_average);
        System.out.println("===============================");

    }
}
