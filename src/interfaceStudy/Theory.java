    /*
It is one of the oops principles. It is purely 100% abstract in nature. Interface is used to declare
only incomplete methods in it.
Features of Interface:
1. Data Members/variables declared inside Interface are by default static and final.
2. Methods-->default public & abstract.
3. The Constructor concept is not present inside Interface.
4. The object of Interface can't be created.
5. Interface supports multiple inheritance.
6. To create object of Interface programmers need to make use of the Implementation class.

Implementation class:
A class which provides definitions for all the incomplete methods which are present in interface with the
help of "implements" keyword is called Implementation class.
Interface-->class   (implements) -->Implementation class
Interface-->Interface (extends) --> Concrete class
class-->class (extends) --> Concrete class
Static Method cannot be abstract
We can complete methods in interface by making the methods default and static.
     */
