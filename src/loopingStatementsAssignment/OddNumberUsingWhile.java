package loopingStatementsAssignment;

public class OddNumberUsingWhile {
    //14. Write a program to print odd numbers between 1 and 20 using while loop.
    static void main(String[] args) {
        int a = 1;
        while (a <= 20) {
            a = a + 2;
            System.out.println(a);
        }
    }
}
