package loopingStatementsAssignment;

public class PrintUsingDoWhile {
    //15. Write a program to print numbers 1 to 5 using do-while loop.
    static void main(String[] args) {
        int a = 1; //initialization
        do {
            System.out.println(a);
            a++;
        } while (a <= 5);
    }
}
